func init() {
	minLat, maxLat = points[0].Lat(), points[0].Lat()
	minLon, maxLon = points[0].Lng(), points[0].Lng()

	for _, p := range points[1:] {
		lat, lon := p.Lat(), p.Lng()
		if lat < minLat {
			minLat = lat
		}
		if lat > maxLat {
			maxLat = lat
		}
		if lon < minLon {
			minLon = lon
		}
		if lon > maxLon {
			maxLon = lon
		}
	}
}

func RandMoscowPoint() (lat, lon float64) {
	for {
		lat = minLat + rand.Float64()*(maxLat-minLat)
		lon = minLon + rand.Float64()*(maxLon-minLon)

		if moscowPoly.Contains(geo.NewPoint(lat, lon)) {
			return lat, lon
		}
	}
}
